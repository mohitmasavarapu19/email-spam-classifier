def add_variation(msg, i):
    variations = [msg, msg.lower(), msg + " thanks", msg + " ok",
                  msg + "!", msg + "...", "hey " + msg, msg + " please"]
    return variations[i % len(variations)]

all_ham  = [add_variation(m, i) for i, m in enumerate(ham  * 35)][:2500]
all_spam = [add_variation(m, i) for i, m in enumerate(spam * 38)][:1500]

data = [("ham", m) for m in all_ham] + [("spam", m) for m in all_spam]
random.shuffle(data)
df = pd.DataFrame(data, columns=["label", "message"])
df.to_csv("data/spam.csv", index=False)
